package org.example;

public class OutPutExamples {
    public static void main(String[] args) {
        float valorNota = 8.7f;
        System.out.println("Sua nota é: " + valorNota);
        System.out.print("Continuando na mesma linha \n");
        System.out.println ("Término do programa.");
        System.out.printf("Sua nota é [usando formatador]: %.2f", valorNota);
    }
}
